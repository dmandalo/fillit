#include "libft.h"

size_t	ft_lstcnt(t_list *alst)
{
	size_t	i;

	i = 0;
	while (alst)
	{
		alst = alst->next;
		if ((i + 1) == 0)
			return (0);
		i++;
	}
	return (i);
}
